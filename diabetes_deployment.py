"""
Diabetes Disease Prediction System - Deployment Module

This module provides production-ready functions to load the trained 
Machine Learning model, imputer, and scaler, and to perform inference 
on new user data with confidence scores.
"""

import joblib
import pandas as pd
import numpy as np
import logging

# Configure basic logging for tracking inference events
logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(message)s",
    datefmt="%Y-%m-%d %H:%M:%S"
)

# Exact feature ordering required by the trained model
FEATURE_NAMES = [
    'Pregnancies', 'Glucose', 'BloodPressure', 'SkinThickness', 
    'Insulin', 'BMI', 'DiabetesPedigreeFunction', 'Age'
]

# Columns where an input of '0' actually means missing data
INVALID_ZERO_COLS = ['Glucose', 'BloodPressure', 'SkinThickness', 'Insulin', 'BMI']

def load_model(model_path="best_diabetes_model.pkl", 
               imputer_path="diabetes_imputer.pkl", 
               scaler_path="diabetes_scaler.pkl"):
    """
    Loads the trained model, imputer, and scaler from disk.
    
    Args:
        model_path (str): Filepath to the tuned model.
        imputer_path (str): Filepath to the fitted SimpleImputer.
        scaler_path (str): Filepath to the fitted StandardScaler.
        
    Returns:
        tuple: (model, imputer, scaler) objects. Returns Nones if failure occurs.
    """
    try:
        model = joblib.load(model_path)
        imputer = joblib.load(imputer_path)
        scaler = joblib.load(scaler_path)
        logging.info("Successfully loaded model, imputer, and scaler from disk.")
        return model, imputer, scaler
    except Exception as e:
        logging.error(f"Failed to load deployment artifacts: {e}")
        return None, None, None

def predict_disease(user_input: dict, artifacts: tuple) -> dict:
    """
    Takes user input, applies the exact saved preprocessing pipeline, 
    and predicts the disease outcome.
    
    Args:
        user_input (dict): A dictionary containing all 8 feature values.
        artifacts (tuple): (model, imputer, scaler) returned by load_model()
        
    Returns:
        dict: The prediction result (Diabetic or Non-Diabetic) and confidence score.
    """
    model, imputer, scaler = artifacts
    
    # 1. Convert input dict to DataFrame to maintain strict column names
    df_input = pd.DataFrame([user_input])
    
    # Ensure columns match the exact training order
    df_input = df_input[FEATURE_NAMES]
    
    # 2. Preprocess: Replace 0s with NaN for specific biological columns
    for col in INVALID_ZERO_COLS:
        if df_input[col].iloc[0] == 0:
            df_input[col] = np.nan
            
    # 3. Preprocess: Impute missing values
    df_input[INVALID_ZERO_COLS] = imputer.transform(df_input[INVALID_ZERO_COLS])
    
    # 4. Preprocess: Scale numerical features exactly as training data was scaled
    df_scaled = pd.DataFrame(scaler.transform(df_input), columns=FEATURE_NAMES)
    
    # 5. Inference: Predict class and exact probabilities
    prediction = model.predict(df_scaled)[0]
    probabilities = model.predict_proba(df_scaled)[0]
    
    # 6. Extract confidence score for the specific predicted class
    confidence = probabilities[prediction] * 100
    
    # Construct final API-ready response dictionary
    result = {
        "prediction_code": int(prediction),
        "prediction_label": "Diabetic" if prediction == 1 else "Non-Diabetic",
        "confidence_score": f"{confidence:.2f}%",
        "probabilities": {
            "Non-Diabetic": f"{probabilities[0]*100:.2f}%",
            "Diabetic": f"{probabilities[1]*100:.2f}%"
        }
    }
    
    return result

